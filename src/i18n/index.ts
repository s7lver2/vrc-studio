export type TranslationKey = keyof typeof translations.en;

// ── Store ─────────────────────────────────────────────────────────────────────

const LOCALE_KEY = "app:locale";

export function loadLocale(): Locale {
  try {
    const v = localStorage.getItem(LOCALE_KEY);
    if (v === "en" || v === "es" || v === "de") return v;
  } catch {}
  const lang = navigator.language.slice(0, 2).toLowerCase();
  if (lang === "es") return "es";
  if (lang === "de") return "de";
  return "en";
}

export function saveLocale(locale: Locale) {
  try { localStorage.setItem(LOCALE_KEY, locale); } catch {}
}

// ── Hook ──────────────────────────────────────────────────────────────────────

let _locale: Locale = loadLocale();
const _listeners = new Set<() => void>();

export function getCurrentLocale(): Locale { return _locale; }

export function setLocale(locale: Locale) {
  _locale = locale;
  saveLocale(locale);
  _listeners.forEach((fn) => fn());
}

import { useSyncExternalStore } from "react";

export function useLocale(): Locale {
  return useSyncExternalStore(
    (cb) => { _listeners.add(cb); return () => _listeners.delete(cb); },
    () => _locale,
  );
}

export function useT() {
  const locale = useLocale();
  return function t(key: TranslationKey, vars?: Record<string, string | number>): string {
    const dict = translations[locale] as Record<string, string>;
    let text = dict[key] ?? (translations.en as Record<string, string>)[key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        text = text.replace(`{${k}}`, String(v));
      }
    }
    return text;
  };
}